    fecha_de_puesta_en_operacion_fpo VARCHAR(100),
    modo_operacion VARCHAR(100),
    modo_de_operacion VARCHAR(100),
    tipo_activo_hidraulico VARCHAR(100),
    tipo_de_proceso_productivo VARCHAR(100),
    rampas_si_no VARCHAR(20),
    unidades_hidraulicas DOUBLE,
    capacidad_hidraulica DOUBLE,
    capacidad_efectiva_neta_mw DOUBLE,
    factor_conversion_hidraulico_mw_m3_s DOUBLE,
    factor_de_conversion_hidraulico_mw_m3_s DOUBLE,
    clasificacion VARCHAR(100),
    minimo_obligatorio_si_no VARCHAR(20),
    arranque_autonomo_si_no VARCHAR(20),
    departamento VARCHAR(100),
    municipio VARCHAR(100),
    subarea VARCHAR(100),
    latitud DOUBLE,
    longitud DOUBLE
);

