CREATE TABLE IF NOT EXISTS vehiculos_ev (
    id INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
    combustible VARCHAR(100),
    estado VARCHAR(100),
    fecha_registro VARCHAR(100),
    anio_registro INT,
    clase VARCHAR(100),
    departamento VARCHAR(100),
    cantidad DOUBLE,
    marca_ev_colombia VARCHAR(255),
    fabricante_bateria_real VARCHAR(255),
    tecnologia_bateria VARCHAR(255),
    capacidad_kwh DOUBLE,
    voltaje_v DOUBLE,
    autonomia_km DOUBLE,
    consumo_wh_km DOUBLE,
    potencia_carga_dc_kw DOUBLE,
    corriente_carga_a DOUBLE,
    impacto_red VARCHAR(255),
    transformador_kva DOUBLE
);

